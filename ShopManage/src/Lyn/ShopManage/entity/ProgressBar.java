package Lyn.ShopManage.entity;

public class ProgressBar {
	private String progressBarName;
	private String progressDescribe;
	private String progressValue;
	private String ProgressLock;
	
	public String getProgressBarName() {
		return progressBarName;
	}
	
	public void setProgressBarName(String progressBarName) {
		this.progressBarName = progressBarName;
	}
	
	public String getProgressDescribe() {
		return progressDescribe;
	}
	
	public void setProgressDescribe(String progressDescribe) {
		this.progressDescribe = progressDescribe;
	}
	
	public String getProgressValue() {
		return progressValue;
	}
	
	public void setProgressValue(String progressValue) {
		this.progressValue = progressValue;
	}
	
	public String getProgressLock() {
		return ProgressLock;
	}
	
	public void setProgressLock(String progressLock) {
		ProgressLock = progressLock;
	}
	
	
}
